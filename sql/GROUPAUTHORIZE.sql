  CONSTRAINT `FK_GROUPAUTHORIZE_PAGENAME_ID` FOREIGN KEY (`PAGENAME_ID`) REFERENCES `PAGENAME` (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=28 DEFAULT CHARSET=latin1;


