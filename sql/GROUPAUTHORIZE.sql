  KEY `fk_GAU_ACCESSNAME` (`ACCESSGROUP_ID`),
  CONSTRAINT `fk_GAU_ACCESSNAME` FOREIGN KEY (`ACCESSGROUP_ID`) REFERENCES `ACCESSGROUP` (`ACCESSGROUP_ID`) ON DELETE NO ACTION ON UPDATE NO ACTION,
